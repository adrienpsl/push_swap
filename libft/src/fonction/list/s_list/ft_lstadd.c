#include "../../../../includes/libft.h"

void	ft_lstadd(t_list **alst, t_list *new)
{
	t_list	*tmp;

	if (!*alst)
		*alst = new;
	else
	{
		tmp = new;
		while (tmp->next)
			tmp = tmp->next;
		tmp->next = *alst;
		*alst = new;
	}
}
