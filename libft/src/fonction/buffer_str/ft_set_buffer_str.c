/*
**	set the buffer with the value of BUFF_SIZE
*/

void	ft_set_buff_str(t_buff_str *buff)
{
	ft_memset(&buff->data, 0, BUFF_SIZE);
	buff->remain_space = BUFF_SIZE;
}
